import { careerData } from './career.js';

// Function to get career data with translations
export function getTranslatedCareerData(language = 'es') {
  const translations = careerTranslations[language] || careerTranslations.es;
  
  return careerData.map(item => {
    const translation = translations[item.id];
    if (translation) {
      return {
        ...item,
        title: translation.title,
        role: translation.role,
        company: translation.company,
        period: translation.period,
        description: translation.description,
        highlights: translation.highlights,
        details: translation.details
      };
    }
    return item;
  });
}

export default { careerTranslations, getTranslatedCareerData };